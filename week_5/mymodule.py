def make_square (n):
    '''
    This square function return a square of a number, it takes a number argument
     '''
    return n **2

def add_two_numbers (a, b):
    '''
    This function takes two number arguments and return the sum
    '''
    return a + b

def make_square_of_array_items(lst):
    '''
    This function takes a list as an argument and it squares each its and return a new list
    '''
    return [i ** 2  for i in lst]


