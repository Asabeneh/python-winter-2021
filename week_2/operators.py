# Operators

# Assignment Operator =
a  = 3
b = 4

# Arithmetic Operators: +, -, *, /, %, //, **
print(a + b) # 7
print(b - a) # 1
print(a * b) # 12
print( a / b) # 0.75
print(b % a) # 1
print(b // a) # 1
print(a ** b) # 3 * 3 * 3 * 3
print(pow(3, 4))

# Logical Operators: and, or, not
# Python is awesome.
# JavaScrit is awesome.
# Python and JavaScript are awesome. => False
# Python or JavaScript is awesome. 
# Python or JavaScrit is not awesome.

print(True and True) # only condition to get True
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(False or True)
print(True or False)
print(False or False) # the only condition to get False
print(not False)
print(not True)

print(2 > 1 and 3 < 4)
print(not 2 < 1 and  3 < 4)

# Comparison Operators: >, >=, <, <=, ==, !=
print(3 > 2)
print(3 >= 2)
print(2 < 3)
print(2 <= 3)
print(2 == 2)
print(2 =='2')
print(str(2) == '2')
print(2 == int('2'))
print(2 != '2')


